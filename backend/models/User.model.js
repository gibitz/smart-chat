import mongoose from "mongoose";
const Schema = mongoose.Schema;

const opts = { timestamps: true }

const userSchema = new Schema(
    {
        username: {
            type: String,
            required: true,
            unique: true,
        },
        email: {
            type: String,
            required: true,
            unique: true,
        },
        fullName: {
            type: String,
            required: true,
        },
        password: {
            type: String,
            required: true,
            minlength: 6,
        },
        profilePic: {
            type: String,
            default: "",
        },
    }, opts
);

const User = mongoose.model("User", userSchema);

export default User;